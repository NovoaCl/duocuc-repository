ALTER TABLE concursos ADD CONSTRAINT concursos_pk PRIMARY KEY ( id );

CREATE TABLE convenciones (
    id                 NUMBER generated by default as identity (START WITH 50 INCREMENT BY 1) NOT NULL,
    nombre             VARCHAR2(500) NOT NULL,
    fec_inicio         DATE NOT NULL,
    fec_termino        DATE NOT NULL,
