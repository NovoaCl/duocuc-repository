ALTER TABLE ciudad ADD CONSTRAINT ciudad_pk PRIMARY KEY ( id );

CREATE TABLE concursos (
    id              NUMBER generated by default as identity (START WITH 50 INCREMENT BY 1) NOT NULL ,
    nombre          VARCHAR2(100) NOT NULL,
    premio          VARCHAR2(500) NOT NULL,
    fecha_hora      DATE NOT NULL,
    tipo_consurso   VARCHAR2(1) NOT NULL,
    convenciones_id NUMBER NOT NULL
