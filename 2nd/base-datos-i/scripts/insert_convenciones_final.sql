ALTER TABLE asis_conv ADD CONSTRAINT asis_conv_pk PRIMARY KEY ( asistentes_rut,
                                                                convenciones_id );

CREATE TABLE asistentes (
    rut              NUMBER NOT NULL,
    dv               VARCHAR2(1) NOT NULL,
    nombre_completo  VARCHAR2(500) NOT NULL,
    correo           VARCHAR2(100) NOT NULL,
    telefono         VARCHAR2(20) NOT NULL,
    fecha_nacimiento DATE NOT NULL,
    pais_id          NUMBER NOT NULL
);

ALTER TABLE asistentes ADD CONSTRAINT asistentes_pk PRIMARY KEY ( rut );

CREATE TABLE categoria (
    id     NUMBER generated by default as identity (START WITH 50 INCREMENT BY 1) NOT NULL ,
    nombre VARCHAR2(100) NOT NULL
