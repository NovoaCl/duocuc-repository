ALTER TABLE convenciones ADD CONSTRAINT convenciones_pk PRIMARY KEY ( id );

CREATE TABLE entradas (
    id              NUMBER generated by default as identity (START WITH 50 INCREMENT BY 1) NOT NULL,
    fecha           DATE NOT NULL,
    tipo_entrada    VARCHAR2(1) NOT NULL,
